"""Binary program templates for .for (Forte/PC3) output.

Templates are extracted from real .for files converted on Kurzweil hardware.
Only the fields listed as patch offsets need to be modified; the rest is
opaque PC3 metadata (effects, MIDI controller names, etc.).
"""

import struct

# --- Patch offset constants ---

# Layer count: 1 byte at this offset in program data
LAYER_COUNT_OFFSET = 0x28

# First layer block starts at this absolute offset in program data
LAYER_BLOCK_START = 0x00E7

# Per-layer block size (318 bytes)
LAYER_BLOCK_SIZE = 318  # 0x13E

# Offsets within each 318-byte layer block
_BLK_LOKEY = 0x00       # 1 byte: lo_key
_BLK_HIKEY = 0x01       # 1 byte: hi_key
_BLK_CAL_KMID = 0x86    # 4 bytes BE32: keymap ID
_BLK_CAL_KMID2 = 0x8A   # 4 bytes BE32: keymap ID (duplicate)
_BLK_LAYER_IDX = 0x91   # 1 byte: layer index (0, 1, 2, ...)
_BLK_FINAL_MARKER = 0x13C  # 2 bytes: 09 00 = non-final, 00 01 = final

# Legacy absolute offsets (used by existing tests)
L1_LYR_LOKEY_OFFSET = LAYER_BLOCK_START + _BLK_LOKEY     # 0x00E7
L1_LYR_HIKEY_OFFSET = LAYER_BLOCK_START + _BLK_HIKEY     # 0x00E8
L1_CAL_KMID_OFFSET = LAYER_BLOCK_START + _BLK_CAL_KMID   # 0x016D
L1_CAL_KMID2_OFFSET = LAYER_BLOCK_START + _BLK_CAL_KMID2 # 0x0171
L2_LYR_LOKEY_OFFSET = LAYER_BLOCK_START + LAYER_BLOCK_SIZE + _BLK_LOKEY   # 0x0225
L2_LYR_HIKEY_OFFSET = LAYER_BLOCK_START + LAYER_BLOCK_SIZE + _BLK_HIKEY   # 0x0226
L2_CAL_KMID_OFFSET = LAYER_BLOCK_START + LAYER_BLOCK_SIZE + _BLK_CAL_KMID  # 0x02AB
L2_CAL_KMID2_OFFSET = LAYER_BLOCK_START + LAYER_BLOCK_SIZE + _BLK_CAL_KMID2  # 0x02AF


def build_program_data(layer_count: int, keymap_ids: list,
                       key_ranges: list) -> bytes:
    """Build .for program data for any number of layers (1-32).

    For 1 layer, uses the 1-layer template directly.
    For 2+ layers, constructs the program from the 2-layer template by
    duplicating per-layer blocks as needed.

    Args:
        layer_count: Number of layers (1-32)
        keymap_ids: List of .for keymap IDs, one per layer
        key_ranges: List of (lo_key, hi_key) tuples, one per layer

    Returns:
        Program data as bytes
    """
    if layer_count <= 0 or layer_count > 32:
        raise ValueError(f"Layer count must be 1-32, got {layer_count}")

    if layer_count == 1:
        return patch_program_template(PROGRAM_1LAYER, 1, keymap_ids, key_ranges)

    # Extract components from the 2-layer template
    prefix = bytearray(PROGRAM_2LAYER[:LAYER_BLOCK_START])       # 0x00 - 0xE6 (231 bytes)
    l1_block = bytearray(PROGRAM_2LAYER[LAYER_BLOCK_START:LAYER_BLOCK_START + LAYER_BLOCK_SIZE])  # L1 reference block
    l2_block = bytearray(PROGRAM_2LAYER[LAYER_BLOCK_START + LAYER_BLOCK_SIZE:LAYER_BLOCK_START + 2 * LAYER_BLOCK_SIZE])  # L2 default block
    suffix = PROGRAM_2LAYER[LAYER_BLOCK_START + 2 * LAYER_BLOCK_SIZE:]  # after all layer blocks

    # Set layer count in prefix
    prefix[LAYER_COUNT_OFFSET] = layer_count

    # Build: prefix + L1 block + (N-1) copies of L2 default block + suffix
    data = bytearray(prefix)
    data.extend(l1_block)
    for _ in range(layer_count - 1):
        data.extend(l2_block)
    data.extend(suffix)

    # Patch each layer block
    for i in range(layer_count):
        blk_start = LAYER_BLOCK_START + i * LAYER_BLOCK_SIZE
        lo, hi = key_ranges[i]
        data[blk_start + _BLK_LOKEY] = lo
        data[blk_start + _BLK_HIKEY] = hi
        struct.pack_into('>I', data, blk_start + _BLK_CAL_KMID, keymap_ids[i])
        struct.pack_into('>I', data, blk_start + _BLK_CAL_KMID2, keymap_ids[i])
        data[blk_start + _BLK_LAYER_IDX] = i
        # Final marker: last layer gets 00 01, others get 09 00
        if i == layer_count - 1:
            data[blk_start + _BLK_FINAL_MARKER] = 0x00
            data[blk_start + _BLK_FINAL_MARKER + 1] = 0x01
        else:
            data[blk_start + _BLK_FINAL_MARKER] = 0x09
            data[blk_start + _BLK_FINAL_MARKER + 1] = 0x00

    return bytes(data)


def patch_program_template(template: bytes, layer_count: int,
                           keymap_ids: list, key_ranges: list) -> bytes:
    """Patch a 1-layer or 2-layer program template with layer-specific values.

    For N>2 layers, use build_program_data() instead.
    """
    data = bytearray(template)
    data[LAYER_COUNT_OFFSET] = layer_count

    # Layer 1
    lo, hi = key_ranges[0]
    data[L1_LYR_LOKEY_OFFSET] = lo
    data[L1_LYR_HIKEY_OFFSET] = hi
    struct.pack_into('>I', data, L1_CAL_KMID_OFFSET, keymap_ids[0])
    struct.pack_into('>I', data, L1_CAL_KMID2_OFFSET, keymap_ids[0])

    # Layer 2
    if layer_count >= 2:
        lo2, hi2 = key_ranges[1]
        data[L2_LYR_LOKEY_OFFSET] = lo2
        data[L2_LYR_HIKEY_OFFSET] = hi2
        struct.pack_into('>I', data, L2_CAL_KMID_OFFSET, keymap_ids[1])
        struct.pack_into('>I', data, L2_CAL_KMID2_OFFSET, keymap_ids[1])

    return bytes(data)


